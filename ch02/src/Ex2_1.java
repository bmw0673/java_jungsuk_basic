class Ex2_1 { 
	public static void main(String args[]) { 
		
		System.out.print("Hello, world");// print() - 출력 후에 줄바꿈을 안함.
		System.out.println("Hello, world");// println() - 출력 후에 줄바꿈을 합니다.
		System.out.printf("%d", 0x16);// printf() - 지시자를 통해 출력형식 지정 가능.
		
		int i = 100; //10진수
		int oct = 0100; //8진수
		int hex = 0x100; //16진수
		
		float f = 3.14f; //f생략 불가
		double d = 3.14d; //생략 가능
		
	} 
}
